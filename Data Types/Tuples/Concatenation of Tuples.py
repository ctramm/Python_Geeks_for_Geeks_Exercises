# Concatenation of Tuples

Tuple1 = (0, 1, 2, 3)
Tuple2 = ('Geeks', 'For', 'Geeks')
Tuple3 = Tuple1 + Tuple2

# Printing first tuple
print("Tuple 1: ")
print(Tuple1)

# Printing second tuple
print("\nTuple 2: ")
print(Tuple2)

# Printing third tuple
print("\nTuples after Concatenation: ")
print(Tuple3)
